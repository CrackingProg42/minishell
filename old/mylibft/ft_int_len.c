#include "libft.h"

int		ft_int_len(long i)
{
	long	len;

	len = (i == 0) ? 1 : 0;
	len += (i < 0) ? 1 : 0;
	while (i != 0)
	{
		i = (long)(i / 10);
		len++;
	}
	return (len);
}
